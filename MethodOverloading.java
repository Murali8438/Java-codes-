package com.murali;

public class MethodOverloading {
	
	public void details(String a, String b, String c ) {

		System.out.println(a + b + c);
		
		
	}
	
	
	
	public void details(int a , char b, int d) {

		System.out.println(a +" "+ b+" " + d);
		
	}
	
	
	public void details( String w, int c) {

		System.out.println(w + c);
		
		
	}
		
	

}
